void yminit();
void ympatch(unsigned char, unsigned char*);
void playscale();

void yminit() {
  unsigned char i, r;
  for (i=0;i<8;i++) {
    r=0xe0+i;
    // set RR=max and then release the voice
    ymwrite(r+0x00,0x0f);
    ymwrite(r+0x08,0x0f);
    ymwrite(r+0x10,0x0f);
    ymwrite(r+0x18,0x0f);
    ymwrite(0x08,i);
    ympatch(i,&tone);
  }
  ymwrite(0x0f,0); //Noise register
  ymwrite(0x14,0); //CTRL register
  ymwrite(0x18,0); // LFO frequency
  ymwrite(0x19,0); // AMD
  ymwrite(0x19,0x80); // PMD
  ymwrite(0x1b,0); // Waveform / CT output pins
}

void ympatch(unsigned char v, unsigned char* patch) {
  uint8_t i;
  ymwrite(0x20+v,*patch);
  ++patch;
  i = 0x38+v;
  while (i>=0x30) {
    ymwrite(i,*patch);
    ++patch;
    i+=8;
  }
}

void playscale() {
  unsigned char delay;
  unsigned char* data = CMAJOR_SCALE;
  yminit();
  while (*data != 0xff) {
    delay = data[0];
    while (delay-- > 0) vsync();
    ymwrite(data[1], data[2]);
    ++data;
    ++data;
    ++data;
  }
}
